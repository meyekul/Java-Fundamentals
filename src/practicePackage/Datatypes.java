package practicePackage;

public class Datatypes{


    //boolean can hold a true and false value
    boolean awesome = true;

    // byte equals 8 bits
    byte byteNum = 12;

    // short equals 16 bits (2 bytes)
    short shortNum = 12;

    // int equals 32 bits (4 bytes)
    int intNum = 12345;

    // long equals 64 bits of memory (8 bytes) - add 'l' to the end of a long number
    long lngNum = 453424534545l;

    // float has 32 bits of memory and can hold decimal numbers, a float is marked by an 'f' at the end
    float floatNum = 45325.56f;

    // double has 64 bits of memory
    double doubleNum = 54353146421.342;

    // char has ...? a char can be indicated by a number e.g. 64, it will read the ASCII chart and assign char the value at 64
    // you can also perform arithmetic type actions on a char
    char aChar = 'a';




}
